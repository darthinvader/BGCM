
class MultilineText:

    def __init__(self, text, Lx1, Ly1, Lx2, Ly2, font):
        self.text = text
        self.Lx1 = Lx1  # The starting x Location
        self.Ly1 = Ly1  # The starting y Location
        self.Lx2 = Lx2  # The ending x Location
        self.Ly2 = Ly2  # The ending y Location
        self.font = font

